class talfoljder():
    
    def geometriskTalfoljd(self, bas, differans, antal):
        svar = bas*(differans**(antal-1))
        return svar

    def aritmetiskTalfoljd(self, bas, differans, antal):
        svar = bas+(differans*(antal-1))
        return svar

